// index.js
const { Client, LocalAuth, Buttons } = require('whatsapp-web.js');
const qrcode = require('qrcode-terminal');

const client = new Client({
  authStrategy: new LocalAuth(),
  puppeteer: {
    headless: true,
    args: ["--no-sandbox", "--disable-setuid-sandbox"]
  }
});

client.on('qr', qr => qrcode.generate(qr, { small: true }));
client.on('ready', () => console.log('✅ Feel Great DZ Bot is running...'));

const state = {}; // لتتبع حالة كل مستخدم

client.on('message', async msg => {
  const chatId = msg.from;
  const text = msg.body.trim();

  // 🟢 الخطوة 1: الترحيب الأول
  if (!state[chatId]) {
    const buttons = new Buttons(
      "👋 مرحباً بك! شرفتنا بالتواصل معنا💚\nأنا ياسين من فريق Feel Great DZ ✅\n\nقبل نبدأ معك، سؤال سريع:\nهل تريد تعرف كيف برنامج Feel Great يساعدك في التخلص من التعب وتنشيط جسمك؟",
      [{ body: "نعم، اشرح لي ✅" }, { body: "لا، فقط السعر 💰" }],
      "Feel Great DZ"
    );
    await msg.reply(buttons);
    state[chatId] = "intro_done";
    return;
  }

  // 🟢 الخطوة 2: اختيار الهدف الرئيسي
  if (state[chatId] === "intro_done" && (text.includes("نعم") || text.includes("لا"))) {
    const buttons = new Buttons(
      "👋 مرحباً بك وشكراً على تواصلك مع فريق ✅ Feel Great DZ\n\nقبل أن نبدأ فقط سؤال سريع حتى نتمكن من مساعدتك بشكل دقيق:\n\nما هو هدفك الرئيسي من التواصل معنا؟",
      [
        { body: "إنقاص الوزن" },
        { body: "رفع مستوى الطاقة" },
        { body: "تنظيم الشهية ومستوى السكر" },
        { body: "تحسين الصحة العامة" },
        { body: "تكيس المبايض" },
        { body: "القولون" },
        { body: "السرطان والمناعة" },
        { body: "ضغط الدم" },
        { body: "مقاومة الأنسولين" }
      ],
      "Feel Great DZ"
    );
    await msg.reply(buttons);
    state[chatId] = "awaiting_goal";
    return;
  }

  // 🟢 الخطوة 3: الدخول في الحالة المناسبة
  if (state[chatId] === "awaiting_goal") {
    if (text === "تنظيم الشهية ومستوى السكر") return sendDiabetesFlow(msg, chatId);
    if (text === "إنقاص الوزن") return sendObesityFlow(msg, chatId);
    if (text === "رفع مستوى الطاقة") return sendEnergyFlow(msg, chatId);
    if (text === "تكيس المبايض") return sendPecosFlow(msg, chatId);
    if (text === "القولون") return sendColonFlow(msg, chatId);
    if (text === "السرطان والمناعة") return sendCancerFlow(msg, chatId);
    if (text === "ضغط الدم") return sendTensionFlow(msg, chatId);
    if (text === "مقاومة الأنسولين") return sendInsulineFlow(msg, chatId);
    if (text === "تحسين الصحة العامة") {
      await msg.reply("برنامج Feel Great موجه لتحسين الصحة العامة عبر توازن السكر والطاقة 🌿");
      return sendMainMenu(msg, chatId);
    }
  }

  // 🟢 زر الرجوع
  if (text.includes("الرجوع إلى القائمة")) {
    return sendMainMenu(msg, chatId);
  }
});


